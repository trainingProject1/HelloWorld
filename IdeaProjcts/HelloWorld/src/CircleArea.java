public class CircleArea {
    public static void main(String[] args) {
        double radius = 4;
        double area = Math.PI * Math.pow(radius, 2);
        System.out.println("При радиусе =" + radius + System.lineSeparator() + "площадь окружности будет =" + area);
    }
}
